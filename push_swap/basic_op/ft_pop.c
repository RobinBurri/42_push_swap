#include "../push_swap.h"

void	ft_pop(t_node **stack)
{
	t_node	*tmp;

	tmp = *stack;
	*stack = (*stack)->n;
	if ((*stack) != NULL)
		(*stack)->p = NULL;
	free(tmp);
}
