#include "../push_swap.h"

int	ft_stklen(t_node *stack)
{
	t_node	*tmp;
	int		i;

	i = 0;
	tmp = stack;
	while (tmp != NULL)
	{
		i++;
		tmp = tmp->n;
	}
	return (i);
}
